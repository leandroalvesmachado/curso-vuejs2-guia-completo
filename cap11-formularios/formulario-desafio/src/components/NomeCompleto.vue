<template>
    <div>
        <Rotulo nome="Nome">
            <input type="text" v-model="value.nome" @input="alterarNome">
        </Rotulo>
        <Rotulo nome="Sobrenome">
            <input type="text" v-model="value.sobrenome" @input="alterarSobreNome">
        </Rotulo>
    </div>
</template>

<script>
import Rotulo from './Rotulo.vue'

export default {
    props: {
        value: {
            type: Object,
            required: true
        }
    },
    components: { Rotulo },
    methods: {
        alterarNome(event) {
            // eventos do tipo input = v-model (proximo)
            this.$emit('input', {
                nome: event.target.value,
                sobrenome: this.value.sobrenome
            });
        },
        alterarSobreNome(event) {
            this.$emit('input', {
                nome: this.value.nome,
                sobrenome: event.target.value
            });
        }
    }
}
</script>

